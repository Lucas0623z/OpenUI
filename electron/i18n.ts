// 键名必须与 settings.language / i18n 资源键一致（pt-BR / es-419 用连字符）
const DICTS: Record<string, Dict> = {
  en,
  zh,
  fr,
  de,
  hi,
  id,
  it,
  ja,
  ko,
  'pt-BR': ptBR,
  'es-419': es419,
  es
}

let currentLang = 'zh'
const langChangeCbs: Array<() => void> = []

/** 注册语言切换回调（可多个订阅者：托盘、应用菜单等） */
export function onLangChange(cb: () => void): void {
  langChangeCbs.push(cb)
}

export function setMainLang(lang: string): void {
  if (!lang || lang === currentLang) return
  if (!(lang in DICTS)) return // 未知语言码：保留当前，避免整体回退英文
  currentLang = lang
  for (const cb of langChangeCbs) cb()
}

/** 主进程取文案；缺失回退英文，再回退 key 本身 */
export function mt(key: string): string {
  return DICTS[currentLang]?.[key] ?? en[key] ?? key
}

/** 当前主进程语言码（供 noEntryPage 的 html lang 等使用） */
export function mainLang(): string {
  return currentLang
}
